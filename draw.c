#include <fractol.h>

void	draw_line(t_fractol *data, int x1, int y1, int x2, int y2)
{
	// y = mx + p
	int	m;
	int	p;

	m = (
		(y2 - y1) == 0 ? 1 : (y2 - y1) 
		/ 
		(x2 - x1) == 0 ? 1 : (x2 - x1)
	);
	p = (m * x1) - y1;
	while (x1 < x2)
	{
		set_pixel_color(data, 
			((m / y1) + p), ((m * x1) + p),
			0x00FF0000
		);
		x1++;
		y1++;
	}

}

